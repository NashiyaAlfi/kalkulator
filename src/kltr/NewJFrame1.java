  public void total(){
      switch(menghitung){
          case 1:
              ans = num + Double.parseDouble(hasil.getText());
              hasil.setText(Double.toString(ans));
              break;
              
               case 2:
              ans = num - Double.parseDouble(hasil.getText());
              hasil.setText(Double.toString(ans));
              break;
              
               case 3:
              ans = num * Double.parseDouble(hasil.getText());
              hasil.setText(Double.toString(ans));
              break;
              
               case 4:
              ans = num / Double.parseDouble(hasil.getText());
              hasil.setText(Double.toString(ans));
              break;
              
                  case 5:
                      
              ans = num / Double.parseDouble(hasil.getText())*100/100;
              hasil.setText(Double.toString(ans));
              break;
                  
          
      }
      
  }   
